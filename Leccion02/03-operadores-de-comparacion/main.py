a = 4
b = 6

# igual a
resultado = a == b
print(f'Resultado == : {resultado}')

# distinto a
resultado = a != b
print(f'Resultado != : {resultado}')

# mayor que
resultado = a > b
print(f'Resultado > : {resultado}')

# mayor o igua que
resultado = a >= b
print(f'Resultado >= : {resultado}')

# menor que
resultado = a < b
print(f'Resultado < : {resultado}')

# menor o igual que
resultado = a <= b
print(f'Resultado: <= {resultado}')