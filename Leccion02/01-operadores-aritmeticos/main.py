n1 = 3
n2 = 2
suma = n1 + n2
# print('resultado suma:', suma)
print(f'resultado suma: {suma}')

# resta
resta = n1 - n2
print(f'resultado reseta: {resta}')

# multiplicacion
multiplicacion = n1 * n2
print(f'resultado multiplicacion: {multiplicacion}')

#divicion
division = n1/n2
print(f'resultado divicion: {division}')

#divicion regresando solamente la parte entera
division = n1//n2
print(f'resultado devision: {division}')

# modulo/residuo
modulo = n1%n2
print(f'resultado residuo de la division: {modulo}')

#exponente
exponente = n1 ** n2
print(f'resultado exponente: {exponente}')